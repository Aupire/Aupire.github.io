char	*ft_strupcase(char *str)
{
	int n;
	int a;

	n = 0;
	a = 0;
	while (str[n] != 0)
	{
		if (96 >= str[n] || str[n] >= 123)
		{
			n++;
		}
		else if (96 < str[n] && str[n] < 123)
		{
			str[n] -= 32;
			n++;
		}
	}
	return (str);
}
