int	ft_str_is_numeric(char *str)
{
	int n;
	int a;

	a = 0;
	n = 0;
	if (str[n] == '\0')
		a = 1;
	while (str[n] != '\0')
	{
		if (47 < str[n] && str[n] < 58)
		{
			a = 1;
		}
		else if (47 >= str[n] || str[n] >= 58)
		{
			return (0);
		}
		n++;
	}
	return (a);
}
