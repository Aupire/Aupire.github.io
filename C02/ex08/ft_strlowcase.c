char	*ft_strlowcase(char *str)
{
	int n;
	int a;

	n = 0;
	a = 0;
	while (str[n] != 0)
	{
		if (65 > str[n] || str[n] > 90)
		{
			n++;
		}
		else if (64 < str[n] && str[n] < 91)
		{
			str[n] += 32;
			n++;
		}
	}
	return (str);
}
