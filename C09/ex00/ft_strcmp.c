int	ft_strcmp(char *s1, char *s2)
{
	int n;
	int c;

	n = 0;
	c = 0;
	while (s1[n] || s2[n])
	{
		if (s1[n] == s2[n])
		{
			n++;
		}
		else if (s1[n] != s2[n])
		{
			c = s1[n] - s2[n];
			return (c);
		}
	}
	return (c);
}
